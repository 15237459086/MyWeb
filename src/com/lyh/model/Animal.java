package com.lyh.model;

import java.util.Date;

public class Animal {

	private Date liyangDate = new Date();
	
	private String animalName = "豆豆";

	public Date getLiyangDate() {
		return liyangDate;
	}

	public void setLiyangDate(Date liyangDate) {
		this.liyangDate = liyangDate;
	}

	public String getAnimalName() {
		return animalName;
	}

	public void setAnimalName(String animalName) {
		this.animalName = animalName;
	}
	
	
}
